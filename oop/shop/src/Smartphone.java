public class Smartphone {
    String brand;
    String model;
    String os;
    int memory;
    int memoryRam;
    int price;

    public Smartphone (String comercialName, String brand, String model, String os, int memory, int memoryRam, int price) {
        this.comercialName = comercialName;
        this.brand = brand;
        this.model = model;
        this.os = os;
        this.memory = memory;
        this.memoryRam = memoryRam;
        this.price = price;
    }
}
